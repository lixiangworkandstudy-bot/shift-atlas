function uniqueTags(tags: ShiftAtlasFocusTag[]) {
  return Array.from(new Set(tags));
}

function scoreTagOverlap(a: ShiftAtlasFocusTag[], b: ShiftAtlasFocusTag[]) {
  const overlap = uniqueTags(a.filter((tag) => b.includes(tag)));
  return {
    overlapTags: overlap,
    score: overlap.length,
  };
}


function normalizeTextForMatching(value: string | null | undefined) {
  return String(value ?? '')
    .toLowerCase()
    .replace(/\s+/g, ' ')
    .trim();
}

export function inferShiftAtlasTagsFromItem(item: ShiftAtlasFetchedItem) {
  const text = normalizeTextForMatching(`${item.title} ${item.summary ?? ''}`);
  const matches = Object.entries(shiftAtlasTagKeywords)
    .filter(([, keywords]) => keywords.some((keyword) => text.includes(keyword.toLowerCase())))
    .map(([tag]) => tag as ShiftAtlasFocusTag);

  return uniqueTags(matches);
}

export function getShiftAtlasItemSourceTags(item: ShiftAtlasFetchedItem) {
  return getShiftAtlasUpdateSourceTags(item.sourceId as ShiftAtlasUpdateSource['id']);
}

function scoreItemAgainstDomains(
  tags: ShiftAtlasFocusTag[],
  sourceTags: ShiftAtlasFocusTag[]
): ShiftAtlasItemDomainFit[] {
  const combinedTags = uniqueTags([...tags, ...sourceTags]);
  return shiftAtlasKnowledgeDomains
    .map((domain) => {
      const domainTags = getShiftAtlasDomainTags(domain.id);
      const { overlapTags, score } = scoreTagOverlap(combinedTags, domainTags);
      const sourceBoost = scoreTagOverlap(sourceTags, domainTags).score * 0.5;
      return {
        domainId: domain.id,
        score: Number((score + sourceBoost).toFixed(1)),
        overlapTags,
      };
    })
    .filter((entry) => entry.score > 0)
    .sort((a, b) => b.score - a.score);
}

function scoreItemAgainstConcepts(
  tags: ShiftAtlasFocusTag[],
  sourceTags: ShiftAtlasFocusTag[]
): ShiftAtlasItemConceptFit[] {
  const combinedTags = uniqueTags([...tags, ...sourceTags]);
  return shiftAtlasConceptPagesWithTags
    .map((concept) => {
      const conceptTags = concept.focusTags ?? [];
      const { overlapTags, score } = scoreTagOverlap(combinedTags, conceptTags);
      const domainBoost = overlapTags.length > 0 ? 0.5 : 0;
      return {
        slug: concept.slug,
        score: Number((score + domainBoost).toFixed(1)),
        overlapTags,
      };
    })
    .filter((entry) => entry.score > 0)
    .sort((a, b) => b.score - a.score);
}

export function assignShiftAtlasItem(item: ShiftAtlasFetchedItem): ShiftAtlasItemAssignment {
  const inferredTags = inferShiftAtlasTagsFromItem(item);
  const sourceTags = getShiftAtlasItemSourceTags(item);
  return {
    item,
    inferredTags,
    sourceTags,
    bestDomains: scoreItemAgainstDomains(inferredTags, sourceTags).slice(0, 3),
    bestConcepts: scoreItemAgainstConcepts(inferredTags, sourceTags).slice(0, 5),
  };
}

export function assignShiftAtlasItems(items: ShiftAtlasFetchedItem[]) {
  return items.map(assignShiftAtlasItem);
}
